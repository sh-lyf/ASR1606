void *findFunc(int crc, struct _scfw_apiMap *buff)
{
    int i = 0;
    struct _scfw_apiMap *table = NULL;

    if (buff) table = buff;
    else table = g_scfw_apiMap;

    if (!table) return NULL;

    for (i = 0; table[i].ptr != NULL; i++)
    {
        if (crc == table[i].crc32)
        {
            return table[i].ptr;
        }
    }

    return NULL;
}

